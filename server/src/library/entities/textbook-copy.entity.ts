import {
  BaseEntity,
  Column,
  CreateDateColumn,
  Entity,
  Index,
  JoinColumn,
  ManyToOne,
  OneToMany,
  PrimaryGeneratedColumn,
  Unique,
  UpdateDateColumn,
} from 'typeorm';
import { DepartmentEntity } from 'src/profiles/entities/department.entity';
import { RoomEntity } from 'src/inventory/entities/room.entity';
import { TeachersEntity } from 'src/profiles/entities/teachers.entity';
import { TextbookTitleEntity } from './textbook-title.entity';
import { TextbookCopyStatus } from '../models/textbook-copy-status.enum';
import { TextbookLoanEntity } from './textbook-loan.entity';

@Entity('textbook_copies')
@Unique(['bookNumber'])
export class TextbookCopyEntity extends BaseEntity {
  @PrimaryGeneratedColumn('uuid')
  id: string;

  @CreateDateColumn()
  createdAt: Date;

  @UpdateDateColumn()
  updatedAt: Date;

  // Unique across whole school. Generated by the system.
  @Index({ unique: true })
  @Column()
  bookNumber: string;

  @Index()
  @Column()
  titleId: string;

  @ManyToOne(() => TextbookTitleEntity, (t) => t.copies, {
    nullable: false,
    onDelete: 'RESTRICT',
  })
  @JoinColumn({ name: 'titleId' })
  title: TextbookTitleEntity;

  @Index()
  @Column()
  departmentId: string;

  @ManyToOne(() => DepartmentEntity, { nullable: false, onDelete: 'CASCADE' })
  @JoinColumn({ name: 'departmentId' })
  department: DepartmentEntity;

  @Index()
  @Column({ nullable: true })
  roomId?: string | null;

  @ManyToOne(() => RoomEntity, { nullable: true, onDelete: 'SET NULL' })
  @JoinColumn({ name: 'roomId' })
  room?: RoomEntity | null;

  @Column({ type: 'enum', enum: TextbookCopyStatus, default: TextbookCopyStatus.Available })
  status: TextbookCopyStatus;

  @Index()
  @Column({ nullable: true })
  assignedTeacherId?: string | null;

  @ManyToOne(() => TeachersEntity, { nullable: true, onDelete: 'SET NULL' })
  @JoinColumn({ name: 'assignedTeacherId' })
  assignedTeacher?: TeachersEntity | null;

  @OneToMany(() => TextbookLoanEntity, (loan) => loan.copy)
  loans: TextbookLoanEntity[];
}

